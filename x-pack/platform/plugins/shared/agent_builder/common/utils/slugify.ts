/**
 * Converts a string to a URL-friendly slug.
 * Lowercases, strips accents, replaces non-alphanumeric with hyphens, trims leading/trailing hyphens.
 */
export function slugify(input: string): string {
  return input
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}
